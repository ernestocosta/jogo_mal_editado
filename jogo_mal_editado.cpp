void monta_mapa2(){
    int l, c;
    for(l=0; l<50; l++){
        for(c=0; c<50; c++){
            if(l==linha && c==coluna) cout<<"&";
            else{
                if(mapa2[l][c]==v) cout<<"  ";
                if(mapa2[l][c]==s) cout<<"==";
                if(mapa2[l][c]==p) cout<<"**",219,219;
                if(mapa2[l][c]==c) cout<<"**",4,4;
            }
        }
        cout<<endl;
    }
    cout<<endl;
}

